package JavaStudy;

public class OperatorExam4 {

	public static void main(String[] args) {
//		비교 연산자의 결과는 boolean이다.
		int i = 10;
		int j = 10;
		
		System.out.println(i == j);
		System.out.println(i != j);
		System.out.println(i < j);
		System.out.println(i <= j);
		System.out.println(i > j);
		System.out.println(i >= j);
		
		i += 10; // i = i + 10;
		System.out.println(i);
		System.out.println(i -= 5);
	}

}
